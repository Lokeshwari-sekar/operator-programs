package com.maven;

public class Assignmentoperator {

	public static void main(String[] args) {
		int a=50,b=30;
		System.out.println(a+=b);//a=a+b
		System.out.println(a-=b);//a=a-b
		System.out.println(a*=b);//a=a*b
		System.out.println(a/=b);//a=a/b
		System.out.println(a%=b);//a=a%b

	}

}

package com.maven;

public class Arithmeticoperators {

	public static void main(String[] args) {
	
		int a=20,b=5;
		System.out.println("The addition of two numbers is:"+(a+b));//addition operation
		System.out.println("The subtraction of two numbers is:"+(a-b));//subtraction operation
		System.out.println("The multiplication of two numbers is:"+a*b);//multiplication operation
		System.out.println("The division of two numbers is:"+a/b);//divide operation -here the quotient value is answer
		System.out.println("The division of two numbers is:"+a%b);//modulus operation- here the remainder value is answer

		
		
	}

}

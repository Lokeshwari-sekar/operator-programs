package com.maven;

public class Bitwiseoperator {

	public static void main(String[] args) {
		int a=10,b=13;
		System.out.println(a&b);// AND operation 
		System.out.println(a|b);//OR operation
		System.out.println(a<<1);//left shift operation
		System.out.println(a>>1);//right shift operation
		System.out.println(~a);//tilde operation
		System.out.println(~b);//tilde operation
		System.out.println(a^b);//XOR operation

	}

}
